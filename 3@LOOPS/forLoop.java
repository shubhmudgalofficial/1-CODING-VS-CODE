public class forLoop {
    public static void main(String[] args) {
        // looping is used basically when we have to repeat the same task multiple times
        // for loop
        // program to print multiple times
        for(int i=0;i<10;++i){
            System.out.println("*");
            /*
              1- initialize int i=0;
              2- check condition i<10;
              3- update ++i
             */
        }

    }
}

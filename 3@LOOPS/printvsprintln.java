
public class printvsprintln {
    public static void main(String[] args) {
        // println ln ---> LINE NEXT
        System.out.println("*"); // prints in next line
        System.out.println("*");
        System.out.println("*");
        System.out.println("*");

        // PRINT
        System.out.print("*"); // prints in same line
        System.out.print("*"); // prints in same line
        System.out.print("*"); // prints in same line
        System.out.print("*"); // prints in same line
    }
}
